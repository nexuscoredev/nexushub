export function visualForDrinkSlug(slug) {
  const spec = IBA_DRINK_VISUALS[slug];
  if (spec) {
    return {
      title: spec.title ?? titleFromSlug(slug),
      glass: spec.glass,
      liquid: spec.liquid,
      garnish: spec.garnish,
    };
  }
  return {
    title: titleFromSlug(slug),
    glass: 'premium crystal cocktail glass',
    liquid: 'beautifully mixed cocktail',
    garnish: 'appropriate fresh garnish',
  };
}

export function tintForDrinkSlug(slug) {
  const spec = IBA_DRINK_VISUALS[slug];
  if (spec?.hue != null) {
    return {
      hue: spec.hue,
      saturation: spec.saturation ?? 1,
      brightness: spec.brightness ?? 1,
    };
  }
  let h = 0;
  for (let i = 0; i < slug.length; i += 1) h = (h * 31 + slug.charCodeAt(i)) >>> 0;
  return {
    hue: h % 360,
    saturation: 0.9 + (h % 25) / 100,
    brightness: 0.92 + (h % 16) / 100,
  };
}
